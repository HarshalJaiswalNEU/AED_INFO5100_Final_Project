    private void populateDonorTable() {
        DefaultTableModel model = (DefaultTableModel) tb2.getModel();
        model.setRowCount(0);

        for (Donor d : ecoSystem.getDonordirectory()) {

            Object[] row = new Object[5];
            row[0] = d.getUname();
            row[1] = d.getName();
            row[2]= d.getDbname();
            model.addRow(row);

        }

    }
    
    private void populateDonorBankTable() {
        DefaultTableModel model = (DefaultTableModel) tb1.getModel();
        model.setRowCount(0);

        for (DonorBank d : ecoSystem.getDonorbankdirectory()) {

            Object[] row = new Object[5];
            row[0] = d.getUsername();
            model.addRow(row);

        }

    }
